                <h4 class="recipeStep"> Step 3: Cook the eggs </h4>
                <p>
                    With the back of a spoon or a ladle, make 4 indentations in the sauce.
                    Break an egg into a cup and carefully pour it into and indentation, being 
                    careful not to break the yolk. Repeat with the remaining eggs. (You can 
                    crack the egg directly into the sauce and skip the cup, but it’s easier 
                    to get a an errant egg shell or protect the dish from a bad egg if you use
                    the cup method.) Sprinkle the feta and olives around the eggs. Cover the pan
                    and simmer over medium heat for 8 minutes, or until the whites are set and
                    the yolks are still soft. (If you like your eggs well done, cook for a
                    minute or two longer.)
                </p>
